    private void CadastraCliente() {
        
            String cpf = txtCpf.getText();
            String nome = txtNome.getText();
            String email = txtEmail.getText();
            String bairro = txtBairro.getText();
            int numero = Integer.parseInt(txtNumero.getText());
            String endereco = txtEndereco.getText();
            String cidade = txtCidade.getText();
            String estado = txtEstado.getText();
            String data_nascimento = txtNascimento.getText();
            String telefone = txtTelefone.getText();
            
            ClienteDTO objClienteDTO = new ClienteDTO(nome, cpf, email, bairro, numero, endereco,cidade,estado, data_nascimento, telefone);
            
            ClienteDAO objPessoaDAO = new ClienteDAO();
            objPessoaDAO.create(objClienteDTO);
            limpaCampos();  
            listaClientes();
            
           
        } 
    
