     private void listaClientes() {
            try{  
         
               ClienteDAO objClienteDAO = new ClienteDAO();
               DefaultTableModel tabelamodel = (DefaultTableModel)TabelaCliente.getModel();
               tabelamodel.setNumRows(0); 
               ArrayList<ClienteDTO> listaClientes = objClienteDAO.readAll();
              for(ClienteDTO Cliente : listaClientes) {
               tabelamodel.addRow(new Object[]{
                 Cliente.getId_cliente(),
                 Cliente.getCpf(), 
                 Cliente.getNome(), 
                 Cliente.getData_nascimento(),
                 Cliente.getEndereco(),
                 Cliente.getBairro(),
                 Cliente.getNumero(),
                 Cliente.getEstado(), 
                 Cliente.getCidade(),
                 Cliente.getTelefone(),
                 Cliente.getEmail()
                 
              
                 
                 
             });
            }
        }catch(Exception e){
            System.out.println("Erro ao listar na tabela" + e);
        }
 }    
     
